int i = 1;

Console.WriteLine ("i =" + i);
Console.WriteLine ("++i = " + ++i); //Pre-Inkrement
Console.WriteLine ("i++ =" + i++); //Post-Inkrement
Console.WriteLine ("i =" + i);
Console.WriteLine ("--i =" + --i); //Pre-Dekrement
Console.WriteLine ("i-- =" + i--); //Post-Dekrement
Console.WriteLine ("i =" + i);